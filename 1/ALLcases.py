#lower case

w="FIRST RULE OF PROGRAMMING: IF ITS WORKS, DONT TOUCH IT"
n=w.lower()
print(n)

#upper case

f="good vibes only"
m=f.upper()
print(m)

#title case

m="don't settle for average"
c=m.title()
print(c)

#capitalize case

z="main rapper nahi ganster hain tu rapper nahi dancer hain"
r=z.capitalize()
print(r)